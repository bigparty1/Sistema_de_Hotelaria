export default class Hotel {
    constructor(hotelId, nome, cidade, endereco, telefone, email, quartos) {
        this.hotelId = hotelId;
        this.nome = nome;
        this.cidade = cidade;
        this.endereco = endereco;
        this.telefone = telefone;
        this.email = email;
        this.quartos = quartos;
    }
}